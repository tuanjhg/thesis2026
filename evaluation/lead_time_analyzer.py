"""
Proactive Lead-Time Analyzer
============================
Quantifies how many seconds the AI forecast triggers mitigation *before*
the confidence-driven reactive path would have. Compares:

    AI (evaluation/results/)           — forecast + detection
    Baseline (evaluation/results_baseline/) — threshold-only (no forecast)

For each scenario with an attack, computes:

    first_proactive_window      — first window with proactive=True (AI)
    first_reactive_window_ai    — first AI window with tier>=3 (reactive-only)
    first_reactive_window_base  — first baseline window with tier>=3
    lead_time_ai_s              — 5s * (first_reactive_ai - first_proactive)
    lead_time_vs_baseline_s     — 5s * (first_reactive_base - first_proactive_ai)

Each window = 5 seconds (spec §4.3).

Produces: evaluation/results/lead_time_comparison.md + .csv
"""

from __future__ import annotations

import csv
import json
import logging
from dataclasses import dataclass, asdict
from pathlib import Path
from typing import List, Optional

WINDOW_S = 5.0

logger = logging.getLogger('lead_time')


@dataclass
class LeadTimeRow:
    scenario:                  str
    n_windows:                 int
    first_proactive_window:    Optional[int]
    first_reactive_ai_window:  Optional[int]
    first_reactive_base_window: Optional[int]
    lead_time_ai_s:            Optional[float]      # reactive_ai − proactive
    lead_time_vs_baseline_s:   Optional[float]      # reactive_base − proactive_ai
    proactive_windows_count:   int


def _load_jsonl(path: Path) -> List[dict]:
    if not path.exists():
        return []
    out = []
    with open(path) as f:
        for line in f:
            if line.strip():
                out.append(json.loads(line))
    return out


def _first_window(records: List[dict], pred) -> Optional[int]:
    for r in records:
        if pred(r):
            return r.get('window_id')
    return None


def analyze_scenario(name: str, ai_dir: Path, base_dir: Path) -> LeadTimeRow:
    ai   = _load_jsonl(ai_dir   / f'{name}.jsonl')
    base = _load_jsonl(base_dir / f'{name}.jsonl')

    first_proactive = _first_window(ai,   lambda r: r.get('proactive'))
    first_react_ai  = _first_window(ai,   lambda r: r.get('tier', 0) >= 3
                                           and not r.get('proactive'))
    first_react_bs  = _first_window(base, lambda r: r.get('tier', 0) >= 3)

    lead_ai = None
    if first_proactive is not None and first_react_ai is not None:
        lead_ai = (first_react_ai - first_proactive) * WINDOW_S

    lead_vs_base = None
    if first_proactive is not None and first_react_bs is not None:
        lead_vs_base = (first_react_bs - first_proactive) * WINDOW_S

    return LeadTimeRow(
        scenario                   = name,
        n_windows                  = len(ai),
        first_proactive_window     = first_proactive,
        first_reactive_ai_window   = first_react_ai,
        first_reactive_base_window = first_react_bs,
        lead_time_ai_s             = lead_ai,
        lead_time_vs_baseline_s    = lead_vs_base,
        proactive_windows_count    = sum(1 for r in ai if r.get('proactive')),
    )


def analyze_all(ai_dir: Path, base_dir: Path, out_dir: Path) -> List[LeadTimeRow]:
    scenarios = sorted({p.stem for p in ai_dir.glob('S?_*.jsonl')})
    rows = [analyze_scenario(s, ai_dir, base_dir) for s in scenarios]

    out_dir.mkdir(parents=True, exist_ok=True)

    # CSV
    csv_path = out_dir / 'lead_time_comparison.csv'
    with open(csv_path, 'w', newline='') as f:
        w = csv.DictWriter(f, fieldnames=list(asdict(rows[0]).keys()))
        w.writeheader()
        for r in rows:
            w.writerow(asdict(r))

    # Markdown table
    md = ["# Proactive Lead-Time Analysis\n",
          "Window size = 5s. `lead_time_ai_s` = how many seconds earlier the AI",
          "forecast pre-positioned (T2) before the AI reactive path (T≥3) would.",
          "`lead_time_vs_baseline_s` = how many seconds earlier the AI forecast",
          "acted compared to the threshold baseline's first T≥3 trigger.\n",
          "| Scenario | First proactive (win) | First reactive AI | First reactive Baseline | Lead vs AI-reactive (s) | Lead vs Baseline (s) | # Proactive |",
          "|---|---|---|---|---|---|---|"]
    for r in rows:
        md.append(
            f"| {r.scenario} | {r.first_proactive_window or '—'} "
            f"| {r.first_reactive_ai_window or '—'} "
            f"| {r.first_reactive_base_window or '—'} "
            f"| {r.lead_time_ai_s if r.lead_time_ai_s is not None else '—'} "
            f"| {r.lead_time_vs_baseline_s if r.lead_time_vs_baseline_s is not None else '—'} "
            f"| {r.proactive_windows_count} |"
        )
    md.append("\n_Generated by `evaluation/lead_time_analyzer.py`._")
    (out_dir / 'lead_time_comparison.md').write_text('\n'.join(md), encoding='utf-8')

    logger.info(f"Wrote {csv_path}")
    logger.info(f"Wrote {out_dir / 'lead_time_comparison.md'}")
    return rows


if __name__ == '__main__':
    import argparse
    logging.basicConfig(level=logging.INFO, format='%(levelname)s: %(message)s')
    root = Path(__file__).resolve().parent.parent
    parser = argparse.ArgumentParser()
    parser.add_argument('--ai-dir',   default=str(root / 'evaluation' / 'results'))
    parser.add_argument('--base-dir', default=str(root / 'evaluation' / 'results_baseline'))
    parser.add_argument('--out-dir',  default=str(root / 'evaluation' / 'results'))
    args = parser.parse_args()
    analyze_all(Path(args.ai_dir), Path(args.base_dir), Path(args.out_dir))
